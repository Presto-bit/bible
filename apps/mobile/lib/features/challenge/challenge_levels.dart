ChallengeLevel bookChallengeLevel(String bookId, String bookName) {
  final id = bookId.toUpperCase();
  final qs = _bookQuestions[id] ??
      [
        _q('${bookId.toLowerCase()}-g1', '读完《$bookName》，你印象最深的是？',
            ['神的创造', '神的信实', '神的救赎', '都需要再读'], 3, '每卷书都指向神的作为。'),
        _q('${bookId.toLowerCase()}-g2', '《$bookName》属于？', ['律法书', '历史书', '诗歌智慧', '新约'], 3,
            '可回到目录确认分类。'),
      ];
  return ChallengeLevel(
    id: 'book-$id',
    title: '$bookName · 巩固关',
    subtitle: '读完本卷后的知识回顾',
    icon: '📖',
    bookId: id,
    questions: qs,
  );
}

List<ChallengeLevel> allChallengeLevels([List<ChallengeLevel>? extra]) =>
