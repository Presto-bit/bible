// 小爱输入框上方 chip 的专业回答结构（system prompt 片段）

export interface AssistantChipDef {
  label: string;
  mode: string;
  q: string;
}

function anchor(ref?: string): string {
  return ref ? `「${ref}」` : '这段经文';
}

/** 静态 chip：解释、应用、查经、译本、原文、讲道 */
export function staticAssistantChips(ref?: string): AssistantChipDef[] {
  const a = anchor(ref);
  return [
    {
      label: '解释经文',
      mode: 'explain',
      q:
        `请用通顺自然的简体中文解释${a}。严格按以下结构输出，每段标题保留：\n` +
        `【经文原意】2–4 句，说清作者意图与上下文。\n` +
        `【关键词】列出 2–3 个核心词并简要释义。\n` +
        `总篇幅 180–280 字，不要输出【应用】或【相关追问】。`,
    },
    {
      label: '生活应用',
      mode: 'apply',
      q:
        `请把${a}应用到今日生活。严格按以下结构输出：\n` +
        `【核心提醒】1–2 句概括经文对今天的信息。\n` +
        `【具体行动】3 条可执行建议（职场/家庭/个人灵修各至少一条，用 ①②③ 编号）。\n` +
        `【祷告方向】1 句简短祷告引导。\n` +
        `不要输出【相关追问】。`,
    },
    {
      label: '预备查经',
      mode: 'understand',
      q:
        `请帮我预备关于${a}的小组查经。严格按以下结构输出：\n` +
        `【经文分段】标出 2–4 个自然段并各用 1 句概括。\n` +
        `【背景要点】历史、作者、听众与上下文（3–5 句）。\n` +
        `【讨论问题】6 个递进式问题：观察 2 个、解释 2 个、应用 2 个。\n` +
        `【带领提示】给查经带领者 2 条注意事项（时间分配或易错点）。\n` +
        `不要输出【相关追问】。`,
    },
    {
      label: '译本对照',
      mode: 'compare',
      q:
        `请对照不同中文译本解释${a}。严格按以下结构输出：\n` +
        `【译本差异】列举和合本与 1–2 个常见译本在措辞上的主要差别。\n` +
        `【差异原因】从原文词义或语法简要说明为何不同（2–4 句）。\n` +
        `【理解建议】给读经者 1–2 句综合理解建议。\n` +
        `不要输出【相关追问】。`,
    },
    {
      label: '原文释义',
      mode: 'original',
      q:
        `请从圣经原文角度解释${a}。严格按以下结构输出：\n` +
        `【原文字词】列出 2–3 个关键原文词（希伯来文/希腊文音译 + 基本义）。\n` +
        `【语法与语境】说明这些词在句中的语法角色与语境含义（3–5 句）。\n` +
        `【神学重点】1–2 句点出由此衍生的核心神学重点。\n` +
        `不要输出【相关追问】。`,
    },
    {
      label: '讲道大纲',
      mode: 'preach',
      q:
        `请为${a}生成讲道大纲要点。严格按以下结构输出：\n` +
        `【主题句】1 句可宣讲的中心信息（15–25 字）。\n` +
        `【经文重述】2–3 句用自己的话重述经文内容。\n` +
        `【大纲】3 个主要论点（每点含：小标题 + 1 句经文依据 + 1 句展开说明）。\n` +
        `【例证建议】每点各 1 个生活化例证方向（不必展开故事）。\n` +
        `【结论与回应】呼召与会众可作出的 1 个具体回应。\n` +
        `不要输出【相关追问】。`,
    },
  ];
}
